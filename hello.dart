void main() {
  // Integer data type
  int age = 25;
  print('Age: $age'); // Output: Age: 25
  
  // Double data type
  double height = 5.11;
  print('Height: $height'); // Output: Height: 5.11
  
  // String data type
  String name = 'John';
  print('Name: $name'); // Output: Name: John
  
  // List data type (dynamic)
  List<dynamic> fruits = ['Apple', 'Banana', 'Orange'];
  print('Fruits: $fruits'); // Output: Fruits: [Apple, Banana, Orange]
  
  // Map data type
  Map<String, dynamic> person = {
    'name': 'Alice',
    'age': 30,
    'height': 5.6
  };
  print('Person: $person'); // Output: Person: {name: Alice, age: 30, height: 5.6}
}
